# 通过key找出对应的value
# 字典的定义使用大括号{}，不过存储元素是一个一个的：键值对
dict0 = {"张贺文": 18, "邢玮": 19, "范家康": 20}
print(f"dict是：{dict0},它的类型是：{type(dict0)}")

# 定义空的字典
dict1 = {}
dict2 = dict()
print(f"dict1是：{dict1},它的类型是：{type(dict1)}")
print(f"dict2是：{dict2},它的类型是：{type(dict2)}")

