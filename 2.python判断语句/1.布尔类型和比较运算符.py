# 布尔类型 真True  假False
result = 10 > 8
print(f"结果是：{result}，数据类型是：{type(result)}")
A = True
B = False
print(f"A的内容是：{A},A的类型是：{type(A)}")
print(f"B的内容是：{B},B的类型是：{type(B)}")
# 比较运算符可得布尔类型
# == ！= <= >= < >
num1 = 10
num2 = 20
print(f"num1等于num2的结果是：{num1 == num2}")
print(f"num1不等num2的结果是：{num1 != num2}")
print(f"num1大于num2的结果是：{num1 > num2}")
print(f"num1小于num2的结果是：{num1 < num2}")
print(f"num1大于等于num2的结果是：{num1 >= num2}")
print(f"num1小于等于num2的结果是：{num1 <= num2}")
